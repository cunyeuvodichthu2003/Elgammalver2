    private void GiaiMa(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GiaiMa
        // TODO add your handling code here:
          if (inputBanMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không có thông điệp cần giải mã!", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            ArrayList<Integer> arr_ban_ma = new ArrayList<>();
            String banMa = inputBanMa.getText();
            int p = Integer.parseInt(inputP.getText().trim());
            int a = Integer.parseInt(inputA.getText().trim());
            for (int i = 0; i < banMa.length(); i++) {
                arr_ban_ma.add((int) banMa.charAt(i));
            }
            //d=y2(y1^a)^-1 mod p
            int k = binhPhuongNhan(arr_ban_ma.get(0), p, a);  //k=y1^a mod p
            String banRo = "";
            for (int j = 1; j < arr_ban_ma.size(); j += 2) {
                //d=y2(k)^-1 mod p
                char c = (char) ((arr_ban_ma.get(j) * ocolitMoRong(p, k)) % p);
                banRo += c;
            }
            outputBanRo.setText(banRo);
        }
    }//GEN-LAST:event_GiaiMa

