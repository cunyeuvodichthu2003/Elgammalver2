    private void Random(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Random
            int max = 45000, min = 8000;
        int p, a, alpha, beta, k;
        Random random = new Random();
        do {
            p = random.nextInt((max - min) + 1) + min;
        } while (checkSNT(p) == false);
        inputP.setText("" + p);
        //set a
        a = random.nextInt((p - 2) + 1) + 2;
        inputA.setText("" + a);
        //set alpha 
        alpha = random.nextInt((p - 1) + 1) + 1;
        inputAlpha.setText("" + alpha);
        //set K
        k = random.nextInt((p - 2) + 1);
        inputK.setText("" + k);
        //set beta 
        beta = binhPhuongNhan(alpha, p, a);
        inputBeta.setText("" + beta);
    }//GEN-LAST:event_Random

