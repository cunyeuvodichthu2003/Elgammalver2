    private boolean checkSNT(int n) {
        if (n < 2) {
            return false;
        }
        for (int i = 2; i <= Math.sqrt(n); i++) {
            if (n % i == 0) {
                return false;
            }
        }
        return true;
    }

    private int binhPhuongNhan(int x, int n, int k) {
        int p = 1;
        String s = Integer.toBinaryString(k);
        for (int i = 0; i < s.length(); i++) {
            if (s.charAt(i) == '1') {
                p = p * p;
                p = p % n;
                p = p * x;
                p = p % n;
            } else {
                p = p * p;
                p = p % n;
            }
        }
        return p;
    }

    private int ocolitMoRong(int ri, int ri1) {
        int q[], s[], t[];
        int ri2 = -1, i = 0, n = ri;
        q = new int[100];
        s = new int[100];
        t = new int[100];
        while (ri2 != 0) {
            q[i + 1] = ri / ri1;
            ri2 = ri % ri1;
            switch (i) {
                case 0:
                    s[i] = 1;
                    t[i] = 0;
                    break;
                case 1:
                    s[i] = 0;
                    t[i] = 1;
                    break;
                default:
                    s[i] = s[i - 2] - q[i - 1] * s[i - 1];
                    t[i] = t[i - 2] - q[i - 1] * t[i - 1];
                    break;
            }
            ri = ri1;
            ri1 = ri2;
            i++;
        }
        s[i] = s[i - 2] - q[i - 1] * s[i - 1];
        t[i] = t[i - 2] - q[i - 1] * t[i - 1];
        return t[i] > 0 ? t[i] : (n + t[i]);
    }

